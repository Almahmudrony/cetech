#pragma once

/*******************************************************************************
**** Includes
*******************************************************************************/

#include "celib/stringid_types.h"


/*******************************************************************************
**** Interface
*******************************************************************************/
namespace cetech {

    /***************************************************************************
    **** Package manager.
    ***************************************************************************/
    namespace package_manager {

        /***********************************************************************
        **** Load package.
        ***********************************************************************/
        void load(StringId64_t name);

        /***********************************************************************
        **** Unload package.
        ***********************************************************************/
        void unload(StringId64_t name);

        /***********************************************************************
        **** Is package is loaded?
        ***********************************************************************/
        bool is_loaded(StringId64_t name);

        /***********************************************************************
        **** Wait for package load done.
        ***********************************************************************/
        void flush(StringId64_t name);
        
        /***********************************************************************
        **** Load boot package.
        ***********************************************************************/
        void load_boot_package();

        /***********************************************************************
        **** Unload boot package.
        ***********************************************************************/
        void unload_boot_package();
    }

    /***************************************************************************
    **** Package manager globals function.
    ***************************************************************************/
    namespace package_manager_globals {

        /***********************************************************************
        **** Init system.
        ***********************************************************************/
        void init();

        /***********************************************************************
        **** Shutdown system.
        ***********************************************************************/
        void shutdown();
    }
}
