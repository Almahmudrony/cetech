#pragma once

/*******************************************************************************
**** Includes
*******************************************************************************/

#include <inttypes.h>

/*******************************************************************************
**** Interface
*******************************************************************************/
namespace cetech {

    /***************************************************************************
    **** Applicaton system.
    ***************************************************************************/
    namespace application {

        /***********************************************************************
        **** Platform enum.
        ***********************************************************************/
        enum Platform {
            PLATFORM_LINUX,
            PLATFORM_DARWIN,
            PLATFORM_WINDOWS,
            PLATFORM_ANDROID,
        };

        /***********************************************************************
        **** Run application main loop.
        ***********************************************************************/
        void run();

        /***********************************************************************
        **** Quit application.
        ***********************************************************************/
        void quit();

        /***********************************************************************
        **** Is application running?
        ***********************************************************************/
        bool is_run();

        /***********************************************************************
        **** Get platform.
        ***********************************************************************/
        Platform platform();

        /***********************************************************************
        **** Get curent frame id.
        ***********************************************************************/
        uint32_t get_frame_id();

        /***********************************************************************
        **** Get delta time.
        ***********************************************************************/
        float get_delta_time();

        /***********************************************************************
        **** Get time from application start.
        ***********************************************************************/
        uint32_t get_ticks();
    }

    /***************************************************************************
    **** Application system globals function.
    ***************************************************************************/
    namespace application_globals {

        /***********************************************************************
        **** Init system.
        ***********************************************************************/
        void init();

        /***********************************************************************
        **** Shutdown system.
        ***********************************************************************/
        void shutdown();
    }
}
